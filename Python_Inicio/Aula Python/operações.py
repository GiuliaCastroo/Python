#Operadores Matemáticos (Aritméticas)#

numero1 = 10
numero2 = 20

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)

print(numero1 // numero2) # Essa divisão com duas barras é uma divisão inteira sem decimais!



print(20 % 3) # Resultado do RESTO da divisão
print(2 ** 3) # Dois elevado a 3 - Potência

