"""
1) Elaborar um programa para calcular o valor de y como função de x, segundo a função y(x)=3x+2,
em um domínio real.
"""

# Solicitar ao usuário o valor de x
x = float(input("Digite o valor de x: "))

# Calcular o valor de y usando a expressão da função
y = 3 * x + 2

# Exibir o resultado
print("O valor de y para x =", x, "é", y)
